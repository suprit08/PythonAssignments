import time

print("Each Element will print after 1 seconds and increment :")

for i in range(1,4):
    print(i)
    time.sleep(1)

print("--------------")

for i in range(1,4):
    print(i)
    time.sleep(2)

print("--------------")

for i in range(1,4):
    print(i)
    time.sleep(3)

