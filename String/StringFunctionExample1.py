print("str.center(30,'*'): ",SomeString.center(30,'*'))
print("-"*50)
print("str.count('hi'):", SomeString.count("hi"))
print("-"*50)
print("Str.encode('utf-8','strict'): ", SomeString.encode('utf-8','strict'))
print("-"*50)
print("str.endswith('suprit') :",SomeString.endswith("suprit"))
