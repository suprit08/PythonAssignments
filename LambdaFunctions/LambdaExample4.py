# All arithmatic operations

add = lambda a,b: a+b
sub = lambda a,b: a-b
mult = lambda a,b: a*b
div = lambda a,b: a/b

a=int(input("Enter the first number:"))
b=int(input("Enter the second number:"))

print("Addition=",add(a,b))
print("Substaction=",sub(a,b))
print("Multiplication=",mult(a,b))
print("Division=",div(a,b))
